export function generateId() {
    return Date.now().toString(36) + Math.random().toString(36).substring(2, 9);
}

export function formatDate(date, format = 'long') {
    const d = date instanceof Date ? date : new Date(date);
    switch (format) {
        case 'iso':
            return d.toISOString().split('T')[0]; // 2026-07-10
        case 'short':
            return d.toLocaleDateString('en-US', { month: 'short', day: 'numeric' });
        case 'medium':
            return d.toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric' });
        case 'long':
            return d.toLocaleDateString('en-US', { weekday: 'long', month: 'long', day: 'numeric', year: 'numeric' });
        case 'time':
            return d.toLocaleTimeString('en-US', { hour: '2-digit', minute: '2-digit' });
        case 'relative':
            return getRelativeTime(d);
        default:
            return d.toLocaleDateString();
    }
}

function getRelativeTime(date) {
    const now = new Date();
    const diff = now - date;
    const mins = Math.floor(diff / 60000);
    const hrs = Math.floor(diff / 3600000);
    const days = Math.floor(diff / 86400000);
    if (mins < 1) return 'Just now';
    if (mins < 60) return `${mins}m ago`;
    if (hrs < 24) return `${hrs}h ago`;
    if (days < 7) return `${days}d ago`;
    return formatDate(date, 'short');
}

export function getToday() {
    const now = new Date();
    const year = now.getFullYear();
    const month = String(now.getMonth() + 1).padStart(2, '0');
    const day = String(now.getDate()).padStart(2, '0');
    return `${year}-${month}-${day}`;
}

export function getGreeting() {
    const hour = new Date().getHours();
    if (hour < 12) return 'Good Morning';
    if (hour < 17) return 'Good Afternoon';
    return 'Good Evening';
}

export function wordCount(text) {
    if (!text) return 0;
    const stripped = text.replace(/<[^>]*>/g, ' ').replace(/\s+/g, ' ').trim();
    return stripped ? stripped.split(' ').length : 0;
}

export function readingTime(text) {
    const words = wordCount(text);
    const minutes = Math.ceil(words / 200);
    return minutes < 1 ? '< 1 min' : `${minutes} min read`;
}

export function stripHtml(html) {
    const tmp = document.createElement('div');
    tmp.innerHTML = html;
    return tmp.textContent || tmp.innerText || '';
}

export function truncate(str, length = 100) {
    if (!str) return '';
    const stripped = stripHtml(str);
    return stripped.length > length ? stripped.substring(0, length) + '…' : stripped;
}

export function debounce(fn, delay = 300) {
    let timer;
    return function (...args) {
        clearTimeout(timer);
        timer = setTimeout(() => fn.apply(this, args), delay);
    };
}

export function throttle(fn, delay = 100) {
    let last = 0;
    return function (...args) {
        const now = Date.now();
        if (now - last >= delay) {
            last = now;
            fn.apply(this, args);
        }
    };
}

export function escapeHtml(text) {
    const map = { '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#039;' };
    return text.replace(/[&<>"']/g, c => map[c]);
}

export function highlightText(text, query) {
    if (!query) return text;
    const escaped = query.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const regex = new RegExp(`(${escaped})`, 'gi');
    return text.replace(regex, '<mark>$1</mark>');
}

export function getDaysInMonth(year, month) {
    return new Date(year, month + 1, 0).getDate();
}

export function getFirstDayOfMonth(year, month) {
    return new Date(year, month, 1).getDay();
}

export function isSameDay(d1, d2) {
    return formatDate(d1, 'iso') === formatDate(d2, 'iso');
}

export function isToday(date) {
    return formatDate(date, 'iso') === getToday();
}

export function daysAgo(n) {
    const d = new Date();
    d.setDate(d.getDate() - n);
    return formatDate(d, 'iso');
}

export function getDateRange(start, end) {
    const dates = [];
    const current = new Date(start);
    const last = new Date(end);
    while (current <= last) {
        dates.push(formatDate(current, 'iso'));
        current.setDate(current.getDate() + 1);
    }
    return dates;
}

